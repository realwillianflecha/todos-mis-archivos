# Operadores de Asignacion en Python
mi_numero = 10
print(f'Asignacion = {mi_numero}')
mi_numero = 20
print(f'Asignacion = {mi_numero}')
# Operador +=
mi_numero += 5 # mi_numero = mi_numero + 5
print(f'Operador += {mi_numero}')
# Operador -=
mi_numero -= 3
print(f'Oeperador -= {mi_numero}')
# Operador *=
mi_numero *= 2
print(f'Operador *= {mi_numero}')
# Operador /=
mi_numero /= 4
print(f'Operador /= {mi_numero}')
# Operador **=
mi_numero **= 2
print(f'Operador **= {mi_numero}')
# Operador %=
mi_numero %= 2
print(f'Operador %= {mi_numero}')