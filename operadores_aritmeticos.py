# Operadores Aritmeticos

# Suma
a = 3 + 4
print(f'Suma {a}')

# Resta
b = 6 - 2
print(f'Resta {b}')

# Multiplicacion
c = a * 2
print(f'Multiplicacion {c}')

# Division
d = b / 2.5
print(f'Division {d}, tipo {type(d)}')

d = b // 2.5
print(f'Division entera (floor) {d}')

# Modulo (residuo de la division)
f = 9 % 2
print(f'Modulo {f}')

# Operacion potencia
e = a ** 2
print(f'Potencia {e}')